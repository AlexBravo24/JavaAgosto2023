package com.condicionales;
import java.util.Scanner;//librería scanner


public class Condicionales13_JICL {

	public static void main(String[] args) {
		/*Una institución benéfica recibe anualmente una donación proveniente de Europa y lo reparte entre un
		centro de salud, un comedor de niños y una parte lo invierte en la bolsa de acuerdo a lo siguiente:

		Si el monto de la donación es de $10000 o más: 30% se destina al centro de salud, 50% al comedor de niños
		y el resto se invierte en la bolsa.
		Si el monto de la donación es menor que $10000: 25% se destina al centro de salud, 60% al comedor de
		niños y el resto se invierte en la bolsa.

		La institución desea saber cuánto de dinero destinará a cada rubro anualmente.
		*/
		Scanner entrada = new Scanner(System.in);

        System.out.print("Ingrese el monto de la donación: ");
        double mtnDonac = entrada.nextDouble();

        double centroSalud;
        double comedorNinos;
        double invBolsa;

        if (mtnDonac >= 10000) {
            centroSalud = mtnDonac * 0.30;
            comedorNinos = mtnDonac * 0.50;
            invBolsa = mtnDonac - centroSalud - comedorNinos;
        } else {
            centroSalud = mtnDonac * 0.25;
            comedorNinos = mtnDonac * 0.60;
            invBolsa = mtnDonac - centroSalud - comedorNinos;
        }

        System.out.println("El monto destinado al centro de salud es: $" + centroSalud);
        System.out.println("El monto destinado al comedor de niños es: $" + comedorNinos);
        System.out.println("El monto destinado a la inversión en la bolsa es: $" + invBolsa);

        entrada.close();
	}

}
