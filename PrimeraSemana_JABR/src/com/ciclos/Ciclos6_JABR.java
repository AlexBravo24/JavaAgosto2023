package com.ciclos;

public class Ciclos6_JABR {

	public static void main(String[] args) {
		/*6.- Una persona desea invertir $1000.00 en un banco, el cual le otorga un 2% de interés mensual. 
		 * Cual será la cantidad de dinero que esta persona tendrá al cabo de un año si todo el dinero lo reinvierte?.
		 * 
		 */

		double inversion=1000;
		
		for (int i=1; i<=12; i++) {
			inversion = inversion + (inversion*0.02);
			System.out.println("La ganancia de inversion el mes " + i + " es de: " + inversion);
//			System.out.printf("%.2f", inversion);
//			System.out.println();
		}
		System.out.println();
		System.out.println("La cantidad a ganar en un año es: ");
		System.out.printf("%.2f",inversion);
	}

}
